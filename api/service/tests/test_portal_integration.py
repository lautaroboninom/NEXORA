    def setUp(self):
        self.client = APIClient()

    def _auth(self):
        self.client.credentials(HTTP_AUTHORIZATION=f"Bearer {PORTAL_TOKEN}")

    def test_missing_or_invalid_token_returns_401(self):
        response = self.client.get("/api/integrations/portal/clientes/1/general/")
        self.assertEqual(response.status_code, 401)

        self.client.credentials(HTTP_AUTHORIZATION="Bearer wrong")
        response = self.client.get("/api/integrations/portal/clientes/1/general/")
        self.assertEqual(response.status_code, 401)

    @override_settings(PORTAL_INTEGRATION_ALLOWED_IPS=["100.64.0.0/10"])
    def test_ip_allowlist_is_enforced_when_configured(self):
        self._auth()
        response = self.client.get(
            "/api/integrations/portal/clientes/1/general/",
            REMOTE_ADDR="127.0.0.1",
        )

        self.assertEqual(response.status_code, 401)

    def test_client_general_uses_nexora_general_rules(self):
        self._auth()
        response = self.client.get("/api/integrations/portal/clientes/1/general/")

        self.assertEqual(response.status_code, 200)
        ids = {item["nexoraIngresoId"] for item in response.data["items"]}
        self.assertEqual(ids, {100, 104})
        self.assertTrue(all(item["companyName"] == "Cliente A" for item in response.data["items"]))

    def test_client_summary_does_not_cross_customer_boundary(self):
        self._auth()
        response = self.client.get("/api/integrations/portal/clientes/2/ingresos/100/summary/")

        self.assertEqual(response.status_code, 404)

    def test_client_budgets_do_not_cross_customer_boundary_and_hide_costs(self):
        self._auth()
        response = self.client.get("/api/integrations/portal/clientes/1/presupuestos/")

        self.assertEqual(response.status_code, 200)
        ids = {item["nexoraIngresoId"] for item in response.data["items"]}
        self.assertEqual(ids, {104})
        self.assertEqual(response.data["items"][0]["status"], "Presupuestado")

        detail = self.client.get("/api/integrations/portal/clientes/1/presupuestos/104/summary/")
        self.assertEqual(detail.status_code, 200)
        self.assertEqual(detail.data["item"]["diagnosis"], "Falla de calibracion detectada")
        self.assertEqual(detail.data["item"]["workToDo"], "Reemplazo y ajuste del modulo de medicion")
        self.assertEqual(detail.data["item"]["items"][0]["description"], "Filtro")
        self.assertNotIn("costo_u_neto", detail.data["item"]["items"][0])
        self.assertNotIn("unitPrice", detail.data["item"]["items"][0])
        self.assertNotIn("subtotal", detail.data["item"]["items"][0])

        blocked = self.client.get("/api/integrations/portal/clientes/2/presupuestos/104/summary/")
        self.assertEqual(blocked.status_code, 404)

