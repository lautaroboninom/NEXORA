    def test_budget_reject_updates_quote_ingreso_and_notifies_only_jefe(self):
        self._auth()
        response = self.client.post(
            "/api/integrations/portal/clientes/1/presupuestos/104/decision/",
            {"decision": "reject", "reason": "No autorizado por compras"},
            format="json",
            HTTP_X_PORTAL_USER_EMAIL="compras@cliente.test",
            HTTP_X_PORTAL_CLIENT_IP="203.0.113.10",
            HTTP_X_PORTAL_USER_AGENT="PortalBrowser/1.0",
        )

        self.assertEqual(response.status_code, 200)
        self.assertEqual(response.data["item"]["status"], "Rechazado")
        with connection.cursor() as cur:
            cur.execute("SELECT estado, fecha_aprobado FROM quotes WHERE id=%s", [1])
            quote_row = cur.fetchone()
            cur.execute("SELECT presupuesto_estado FROM ingresos WHERE id=%s", [104])
            ingreso_row = cur.fetchone()
            cur.execute("SELECT user_id, notification_key, payload FROM notifications ORDER BY user_id")
            notifications = cur.fetchall()

        self.assertEqual(quote_row[0], "rechazado")
        self.assertIsNotNone(quote_row[1])
        self.assertEqual(ingreso_row[0], "rechazado")
        self.assertEqual([row[0] for row in notifications], [8])
        self.assertEqual(notifications[0][1], "presupuesto_decision_portal")
        self.assertIn("203.0.113.10", str(notifications[0][2]))
        self.assertIn("PortalBrowser/1.0", str(notifications[0][2]))

    def test_budget_approve_updates_quote_ingreso_and_rejects_second_decision(self):
        self._auth()
        response = self.client.post(
            "/api/integrations/portal/clientes/1/presupuestos/104/decision/",
            {"decision": "approve"},
            format="json",
            HTTP_X_PORTAL_USER_EMAIL="compras@cliente.test",
        )

        self.assertEqual(response.status_code, 200)
        self.assertEqual(response.data["item"]["status"], "Aprobado")
        with connection.cursor() as cur:
            cur.execute("SELECT estado FROM quotes WHERE id=%s", [1])
            self.assertEqual(cur.fetchone()[0], "aprobado")
            cur.execute("SELECT presupuesto_estado FROM ingresos WHERE id=%s", [104])
            self.assertEqual(cur.fetchone()[0], "aprobado")

        second = self.client.post(
            "/api/integrations/portal/clientes/1/presupuestos/104/decision/",
            {"decision": "reject", "reason": "Tarde"},
            format="json",
        )
        self.assertEqual(second.status_code, 409)

